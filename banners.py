def combined_banner():
    # Split each banner into lines
    paras_lines = paras_banner1().splitlines()
    conk_lines = conkeldurr_banner2().splitlines()

    # Make both lists the same length (pad shorter one with blanks)
    max_len = max(len(paras_lines), len(conk_lines))
    paras_lines += [""] * (max_len - len(paras_lines))
    conk_lines += [""] * (max_len - len(conk_lines))

    # Combine line by line, with some spacing between
    spacing = "|"  # adjust gap
    combined = [p + spacing + c for p, c in zip(paras_lines, conk_lines)]

    # Join and print the full banner
    print("\n".join(combined))

